package ca.bcit.comp2522.lab4;

/**
 * The Reversible interface provides a method to display a reversed version of an object.
 * Implementing classes should provide their own definition of how the object is reversed.
 *
 * @author Dalraj Bains
 * @author Farzad Farzin
 * @author Anil Bronson
 * @author Arsh Mann
 * @version 1.0
 */
public interface Reversible {

    /**
     * Displays the reversed version of the object.
     *
     * @return a String representing the reversed object.
     */
    String displayReversed();
}
